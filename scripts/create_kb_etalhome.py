# ─── СТВОРЕННЯ ТАБЛИЦІ ────────────────────────────────────────────────────────

def get_client():
    creds_json = os.getenv("GOOGLE_CREDENTIALS_JSON")
    if not creds_json:
        raise ValueError("GOOGLE_CREDENTIALS_JSON не знайдено в .env")
    creds = Credentials.from_service_account_info(
        json.loads(creds_json), scopes=SCOPES
    )
    return gspread.authorize(creds)


def fill_sheet(spreadsheet, title: str, data: list[list]) -> None:
    try:
        ws = spreadsheet.worksheet(title)
        ws.clear()
    except gspread.WorksheetNotFound:
        ws = spreadsheet.add_worksheet(title=title, rows=200, cols=10)
    ws.update(data)
    # Заморозити перший рядок (заголовки)
    ws.freeze(rows=1)
    print(f"  ✅ Аркуш '{title}': {len(data)-1} рядків")


def main():
    print("🚀 Створення Knowledge Base для etalhome...")

    gc = get_client()

    sheet_id = "1EELiSfK3tA_k3KxEjEte-VHQFPUDfMuTGEfzOBMsyC4"
    spreadsheet = gc.open_by_key(sheet_id)
    print(f"📊 Таблиця відкрита: {spreadsheet.url}")

    # Заповнюємо аркуші
    fill_sheet(spreadsheet, "Info", INFO)
    fill_sheet(spreadsheet, "Products", PRODUCTS)
    fill_sheet(spreadsheet, "Prices", PRICES)
    fill_sheet(spreadsheet, "Calculation_Rules", CALCULATION_RULES)
    fill_sheet(spreadsheet, "FAQ", FAQ)

    # Видаляємо стандартний порожній аркуш Sheet1
    try:
        spreadsheet.del_worksheet(spreadsheet.worksheet("Sheet1"))
    except Exception:
        pass

    print(f"\n✅ KB готова!")
    print(f"📋 Sheet ID: {sheet_id}")
    print(f"🔗 URL: {spreadsheet.url}")


if __name__ == "__main__":
    main()
